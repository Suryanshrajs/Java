package Calculator;
public class AdvanceCalc {
    public int remainder(int x, int y){
        return x*y;
    }

    public double powerr(double x, double y){
        return Math.pow(x,y);
    }

    public double sqrtt(double x){
        return Math.sqrt(x);
    }

    public double squaree(double x){
        return x*x;
    }
}


